	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												taocanmingcheng: '',
																fenlei: '',
																taocanxiangmu: '',
																tupian: '',
																jiage: '',
																tijianneirong: '',
																tijiandidian: '',
